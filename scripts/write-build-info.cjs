const { execSync } = require('node:child_process');
const fs = require('node:fs');
const path = require('node:path');

function sh(cmd) {
  return execSync(cmd, { stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim();
}

function safeGit(cmd, fallback = '') {
  try {
    return sh(cmd);
  } catch {
    return fallback;
  }
}

const gitSha = safeGit('git rev-parse --short HEAD', 'unknown');
const gitBranch = safeGit('git rev-parse --abbrev-ref HEAD', 'unknown');
const buildTimeIso = new Date().toISOString();

const outPath = path.join(process.cwd(), 'src', 'lib', 'buildInfo.ts');
const content =
  `// This file is auto-generated by scripts/write-build-info.cjs\n` +
  `// Do not edit manually.\n` +
  `\n` +
  `export const BUILD_GIT_SHA = ${JSON.stringify(gitSha)};\n` +
  `export const BUILD_GIT_BRANCH = ${JSON.stringify(gitBranch)};\n` +
  `export const BUILD_TIME_ISO = ${JSON.stringify(buildTimeIso)};\n`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, content, 'utf8');
console.log(`[buildInfo] ${gitSha} @ ${buildTimeIso}`);

